class RPG::Armor
  include EBJB
  
  #//////////////////////////////////////////////////////////////////////////
  # * Properties
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Get Nature rate bonus
  #--------------------------------------------------------------------------
  # GET
  def nature_rate_bonus
    bonus = BATTLECOMMANDS_CONFIG::BC_STEAL_ARMOR_BONUS_ID[self.id]
    if bonus == nil
      bonus = 0
    end
    return bonus
  end
  
end
