#==============================================================================

class BlitzFilter < Filter
  
  #//////////////////////////////////////////////////////////////////////////
  # * Public Methods
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Apply method
  #     x : object to filter
  #--------------------------------------------------------------------------
  def apply(x, index)
    if x != nil && index != nil
       
      case mode
        when "input"
          return applyInput(x, index)
        #when
          #...
        else
          return true
      end
      
    else
      return nil
    end
  end
  
  #//////////////////////////////////////////////////////////////////////////
  # * Private Methods
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Apply method (using the name property)
  #     x : object to filter
  #     index : index to check
  #--------------------------------------------------------------------------
  def applyInput(x, index)
    if x[1][index] == @value
      return true
    else
      return false
    end
  end
  private:applyInput
  
end